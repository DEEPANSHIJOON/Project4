# empty range
print(list(range(0)))

# using the range(stop)
print(list(range(10)))

# using the range(start, stop)
print(list(range(4,6)))

# using the range(start, stop , step)
print(list(range(4,15,3)))

# using range to print with negative index
# using the range(start, stop , -step)
print(list(range(15,3,-2)))



